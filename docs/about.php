          their hands on nice paper and colouring materials that they pour
          their hearts out. We also offer activities developed by Doorstep
          School, Pune, to improve children's reading skills. Children enjoy
          practicing their reading, public speaking, and preforming skills.
          Girls are winning competitions in schools, are publishing an
          Amravati newspaper, and are developing a wide interest that is
